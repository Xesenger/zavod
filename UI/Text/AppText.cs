using System;
using System.Collections.Generic;
using System.Globalization;

namespace zavod.UI.Text;

public enum UiLanguage
{
    English,
    Russian
}

public static class UiLanguageResolver
{
    public const string EnvironmentVariable = "ZAVOD_UI_LANG";

    public static UiLanguage Resolve()
    {
        var value = Environment.GetEnvironmentVariable(EnvironmentVariable)?.Trim();
        return string.Equals(value, "ru", StringComparison.OrdinalIgnoreCase)
            ? UiLanguage.Russian
            : UiLanguage.English;
    }
}

public static class AppText
{
    private static UiTextCatalog _current = UiTextCatalog.English;

    public static UiTextCatalog Current => _current;

    public static UiLanguage CurrentLanguage { get; private set; } = UiLanguage.English;

    public static void Use(UiLanguage language)
    {
        CurrentLanguage = language;
        _current = language == UiLanguage.Russian
            ? UiTextCatalog.Russian
            : UiTextCatalog.English;
    }
}

public sealed class UiTextCatalog
{
    private readonly IReadOnlyDictionary<string, string> _texts;

    private UiTextCatalog(UiLanguage language, IReadOnlyDictionary<string, string> texts)
    {
        Language = language;
        _texts = texts;
    }

    public UiLanguage Language { get; }

    public string LanguageCode => Language == UiLanguage.Russian ? "ru" : "en";

    public string Get(string key)
    {
        if (_texts.TryGetValue(key, out var value))
        {
            return value;
        }

        throw new KeyNotFoundException($"Missing UI text key '{key}' for language '{LanguageCode}'.");
    }

    public string Format(string key, params object?[] args)
    {
        return string.Format(CultureInfo.InvariantCulture, Get(key), args);
    }

